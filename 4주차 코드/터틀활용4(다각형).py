import turtle
t= turtle.Turtle()
t.shape("circle")
t.width(3)
t.color("saddlebrown")
for i in range(3):
    t.lt(120)   
    t.fd(100)
    t.stamp()
t.fillcolor("rosybrown")
t.begin_fill()
for i in range(2):
    t.fd(100)
    t.lt(120)   
    t.fd(100)
    t.lt(60)
t.end_fill()
t.fd(25)
t.lt(120)
t.fd(100)
t.rt(120)
t.fd(25)
t.rt(60)
t.fd(100)
t.lt(60)
t.fd(25)
t.lt(120)
t.fd(100)

